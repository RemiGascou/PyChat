def isValidIP(ip:str) -> bool:
    if len(ip) >= 3+4 and len(ip)<= 3*4+3 and ip.count(".") == 3:
        ip = ip.split(".")
        for e in ip:
            if e.isdecimal():
                e = int(e)
                if e < 0 or e > 255:
                    return False
            else:
                return False
        return True
    else :
        return False


